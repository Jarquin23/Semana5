calificación = float(input("Ingrese la calificación del estudiante (0-10): "))
if calificación >= 9 and calificación <= 10:
    print("La calificación es A")
elif calificación >= 7 and calificación <= 8:
    print("La calificación es B")
elif calificación >= 5 and calificación <= 6:
    print("La calificación es C")
elif calificación >= 3 and calificación <= 4:
    print("La calificación es D")
elif calificación >= 1 and calificación <= 2:
    print("La calificación es F")